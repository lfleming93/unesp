    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (venda.getProdutos() == null || venda.getCliente() == null) {
          JOptionPane.showMessageDialog(rootPane, 
      "Para efetuar a compra selecione um cliente e insira ao menos um produto!",
            "Erro!", JOptionPane.ERROR_MESSAGE);
        } else {
          VendaEntity ve = new VendaEntity();
          
          if(ve.inserir(venda) != null) {
            JOptionPane.showMessageDialog(rootPane, "Venda efetuada com sucesso.", 
            "Sucesso!", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
            
          } else {
            JOptionPane.showMessageDialog(rootPane, "Falha ao salvar! Por favor tente mais tarde!",
            "Erro!", JOptionPane.ERROR_MESSAGE);
          }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    public void inserirProduto(Produto produto, int qtd) {
      double valor = qtd * produto.getValor();
      VendaProduto vp = new VendaProduto();
      vp.setProduto(produto);
      vp.setQuantidade(qtd);
      vp.setValor(valor);
      venda.setProduto(vp);
      DefaultTableModel modelo = (DefaultTableModel) jTable2.getModel();
      Object[] linha = new Object[]{produto.getNome(), 
                        qtd, valor};  
      modelo.addRow(linha);
    }


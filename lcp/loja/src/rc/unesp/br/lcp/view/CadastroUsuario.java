    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
      Usuario u = new Usuario();
        
        u.setNome(jTxtNome.getText());
        u.setCpf(jTxtCpf.getText());
        u.setEmail(jTxtEmail.getText());
        u.setSenha(new String(jPassword.getPassword()));
        
        CadastroClienteController cc = new CadastroClienteController();
        
        if (cc.cadastrarAdmin(u)) {
          JOptionPane.showMessageDialog(rootPane, "Cliente salvo com sucesso", 
            "Sucesso!", JOptionPane.INFORMATION_MESSAGE);
          this.setVisible(false);
        } else {
          JOptionPane.showMessageDialog(rootPane, "Erro ao cadastrar, por favor preencha todos os campos",
            "Erro!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed


